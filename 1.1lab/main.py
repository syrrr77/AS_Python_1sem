def f(x):
    return x**3 - 27*x**2 + 13

# Проверяем значения функции в точках от 0 до 500 с шагом 0.1 для очень приближенного результата
min_y = None
min_x = None

for x in [i * 0.1 for i in range(0, 500)]:
    y = f(x)
    if (min_y is None) or (y < min_y):
        min_y = y
        min_x = x

print(f"Примерная точка минимума: x = {min_x}")

